// Tipos de funções

//Declarativas
function funcaoDeclarativa() {
  console.log("Sou uma mensagem de uma função declarativa");
}
funcaoDeclarativa();

// Expressão de funçaõ
var funcao = function () {
  console.log("Sou uma mensagem de uma função de expressão");
};
funcao();

//Arrow function
var funcaoArrow = () => {
  console.log("Sou uma arrow function");
};
funcaoArrow();
